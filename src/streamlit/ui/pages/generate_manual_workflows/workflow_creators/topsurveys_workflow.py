"""
TopSurveys Workflow Creator  v1.0.0
=====================================
Builds a single continuous-loop Automa JSON workflow that:
  1. Opens the survey URL
  2. Loops over every page answering ALL inputs (screener + full survey)
  3. Detects disqualification phrases and exits cleanly
  4. Detects survey completion and marks success

The answers pre-generated by Gemini (stored in the DB) are baked into the
workflow as a JS lookup table so Automa can answer known questions without
calling any API at runtime. Unknown questions (post-screener body) are
handled by a generic mid-option fallback.
"""

