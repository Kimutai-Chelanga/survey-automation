# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _bid() -> str:
    """Short unique block ID."""
    return uuid.uuid4().hex[:8]


def _mid_option(options) -> Optional[str]:
    if not options:
        return None
    if isinstance(options, str):
        try:
            options = json.loads(options)
        except Exception:
            return None
    return str(options[len(options) // 2]) if options else None


def _make_block(name: str, label: str, data: dict,
                dual_output: bool = False) -> dict:
    """Create a standard Automa block dict."""
    outputs = {"output-1": {"connections": []}}
    if dual_output:
        outputs["output-2"] = {"connections": []}
    return {"id": _bid(), "name": name, "label": label,
            "data": data, "outputs": outputs}


def _connect(blocks: List[dict], from_id: str, to_id: str,
             port: str = "output-1") -> None:
    for b in blocks:
        if b["id"] == from_id:
            b["outputs"][port]["connections"].append(
                {"node": to_id, "output": "output-1"}
            )
            return


